export function translate(key: keyof typeof translations['en'], lang: Language, params?: (string | number)[]): string {
  const dict = translations['it'];
  let val = dict[key] || translations['en'][key] || '';
  if (params && params.length > 0) {
    params.forEach((param, index) => {
      val = val.replace(`{${index}}`, String(param));
    });
  }
  return val;
}

export function translateTeam(name: string, lang: Language): string {
  return EN_IT_TEAMS[name] || name;
}
